public class For_Loops {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            System.out.println("Hello World");
        }
        for (int i = 0; i < 5; i++) {
            System.out.print(i+" ");
        }
        System.out.println("");
        for (int i = 0; i < 5; i++) {
            System.out.println("Hello World " +i);
        }
        for (int i = 5; i > 0; i--) {
            System.out.println("Hello World " +i);
        }
    }
}
