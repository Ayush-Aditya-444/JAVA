public class How_To_Convert_String_Into_Number {
    public static void main(String[] args) {
        int a = 1;
        String b="6", d="7.7";
        int c=Integer.parseInt(b)+a;
        double e=Double.parseDouble(d)+a;
        System.out.println(c);
        System.out.println(e);
    }
}
