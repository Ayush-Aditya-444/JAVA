public class Casting {
    public static void main(String[] args) {
        int a = 1, b = 2;
        double c=1.1;
        int d=a+(int)c;
        String e="6";
        int f=Integer.parseInt(e)+a;
        System.out.println(a+b);
        System.out.println(a+c);
        System.out.println(d);
        System.out.println(f);

    }
}
